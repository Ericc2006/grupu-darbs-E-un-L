def sienuaprekinasana():
    siengar = float(input("Ievadi sienas garumu m - "))
    sienplat = float(input("Ievadi sienas  platumu m - "))
    sienaug = float(input("Ievadi sienas augstumu m - "))
    return (siengar * sienaug) * 2 + (sienplat * sienaug) * 2

